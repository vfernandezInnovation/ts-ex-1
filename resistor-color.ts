export const colorCode = (color : string) => {
  const colors : String[] = 
  ['black',
  'brown',
  'red',
  'orange',
  'yellow',
  'green',
  'blue',
  'violet',
  'grey',
  'white'];
  return colors.indexOf(color.toLocaleLowerCase())
}

export const COLORS = 
['black',
'brown',
'red',
'orange',
'yellow',
'green',
'blue',
'violet',
'grey',
'white'];
